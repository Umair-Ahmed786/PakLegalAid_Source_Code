import re

def extract_assistant_response(text):
    # Define a pattern to look for the question and assistant's answer
    pattern = r'(\?)(.*?)(?=\n|$)(.*)'  # Pattern for matching question and response after it.

    # Search for the question mark and assistant's response after it
    match = re.search(r'\?(.+?)(?=\n|$)(.*)', text, re.DOTALL)

    if match:
        return match.group(2).strip()  # Return only the assistant's response after the question.
    else:
        return "No assistant response found."

