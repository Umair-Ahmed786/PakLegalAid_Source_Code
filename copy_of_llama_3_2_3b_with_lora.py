def generate_legal_response(query: str):
    # Tokenize input
    inputs = tokenizer(query, return_tensors="pt").to("cuda" if torch.cuda.is_available() else "cpu")

    # Generate response
    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens=150,
            do_sample=True,
            temperature=0.7,
            pad_token_id=tokenizer.eos_token_id  # to avoid warning
        )

    # Decode full output
    full_output = tokenizer.decode(outputs[0], skip_special_tokens=True)

    # Remove the input query from the output if it exists
    if full_output.lower().startswith(query.lower()):
        response_only = full_output[len(query):].strip()
    else:
        response_only = full_output.strip()

    return response_only

