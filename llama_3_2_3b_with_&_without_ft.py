"""##Generating Response"""

def format_prompt(query):
    return f"<|system|>\nYou are a helpful legal expert.\n<|user|>\n{query}\n<|assistant|>"

# Create a list of formatted prompts
prompts = [format_prompt(q) for q in test_df["Query"]]

# Tokenize all prompts
tokens = tokenizer(prompts, return_tensors="pt", padding=True).to("cuda")

# Generate responses
outputs = model.generate(**tokens, max_new_tokens=200)

# Decode responses
generated_responses = tokenizer.batch_decode(outputs, skip_special_tokens=True)

# Extract only model's part of the response
import re
def extract_response(text):
    parts = re.split(r"<\|assistant\|>", text)
    return parts[-1].strip() if len(parts) > 1 else text.strip()

test_df["generated_response"] = [extract_response(r) for r in generated_responses]

