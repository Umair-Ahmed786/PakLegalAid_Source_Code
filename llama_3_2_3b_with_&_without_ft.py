from langchain_community.vectorstores import FAISS
from langchain_community.embeddings import HuggingFaceEmbeddings
from langchain.docstore.document import Document
import pandas as pd

# Load the dataset
df = pd.read_csv("/content/splited_train_dataset.csv")

# Combine question + answer into a single chunk
documents = []
for _, row in df.iterrows():
    question = row["Query"]
    response = row["Response"]
    combined = f"❓ Question: {question}\n✅ Answer: {response}"
    documents.append(Document(page_content=combined, metadata={"source": "lawyer_faq"}))

documents[0]

documents[3]

# Load the embedding model
embedding_model = HuggingFaceEmbeddings(model_name="BAAI/bge-base-en-v1.5")

# Create FAISS index
vector_db = FAISS.from_documents(documents, embedding=embedding_model)

