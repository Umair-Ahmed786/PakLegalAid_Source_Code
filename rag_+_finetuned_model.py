# Define the text cleaning function
def clean_text(text):
    if pd.isna(text):
        return ""
    text = re.sub(r'\s+', ' ', str(text))  # remove extra whitespaces
    text = text.replace('\xa0', ' ')       # fix non-breaking space
    return text.strip()

