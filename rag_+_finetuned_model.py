import re

def extract_markdown_answer(response_text):
    """
    Extracts clean text after '### 🧾 Answer' and '<|assistant|>' from a Markdown-styled response.
    """
    pattern = r"### 🧾 Answer\s*<\|assistant\|>\s*(.*)"
    match = re.search(pattern, response_text, re.DOTALL)
    return match.group(1).strip() if match else "No valid answer found."

